a=int(input("enter the number:"))
b=int(input("enter the number:"))
bit_and=a & b
print(f"bitwise and:{bit_and}")
bit_or=a | b
print(f"bitwise or:{bit_or}")
bit_xor=a ^ b
print(f"bitwise xor:{bit_xor}")
bit_leftshift=a << 1
print(f"bitwise leftshift:{bit_leftshift}")
bit_rightshift=a >> 1
print(f"bitwise rightshift:{bit_rightshift}")